    private void jbtnRank1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnRank1ActionPerformed
//JOptionPane.showMessageDialog(rootPane, "All your Data has been saved");

        double [] R = new double[14];

R[0]=Double.parseDouble(jtxtMaths.getText());
R[1]=Double.parseDouble(jtxtEng.getText());
R[2]=Double.parseDouble(jtxtBio.getText());
R[3]=Double.parseDouble(jtxtcom.getText());
R[4]=Double.parseDouble(jtxtchem.getText());
R[5]=Double.parseDouble(jtxtphys.getText());
R[6]=Double.parseDouble(jtxtFm.getText());
R[7]=Double.parseDouble(jtxtHis.getText());

R[8] = (R[1] + R[2]+R[3] + R[4]+R[5] + R[6]+R[7])/8;
R[9] = (R[1] + R[2]+R[3] + R[4]+R[5] + R[6]+R[7]);

String Average = String.format("%.0f",R[8]);
jtxtAve.setText(Average);

String TotalScore = String.format("%.0f",R[9]);
jtxtTS.setText(TotalScore);

if(R[9] >=700)
{
    jtxtRank.setText("Excellent");
}
else if(R[9] >=600)
{
    jtxtRank.setText("Very Good");
}
else if(R[9] >=500)
{
    jtxtRank.setText("Good");
}
else if(R[9] >=400)
{
    jtxtRank.setText("pass");
}
else if(R[9] >=200)
{
    jtxtRank.setText("fail");
}
DefaultTableModel model= (DefaultTableModel) jTable1.getModel();
model.addRow(new Object[]{ Student.getSelectedItem().toString(),jtxtFN.getText(),jtxtSn.getText(),
    jComboBox.getSelectedItem().toString(),jtxtMaths.getText(),jtxtEng.getText(),
    jtxtBio.getText(),jtxtcom.getText(),jtxtchem.getText(),jtxtphys.getText(),
    jtxtHis.getText(),jtxtFm.getText(),jtxtTS.getText(),jtxtAve.getText(), 
    jtxtRank.getText(),});

// TODO add your handling code here:
    }//GEN-LAST:event_jbtnRank1ActionPerformed

